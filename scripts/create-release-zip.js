"use strict";

const fs = require("node:fs");
const path = require("node:path");
const { execFileSync } = require("node:child_process");

const ROOT_DIR = path.resolve(__dirname, "..");
const PACKAGE_JSON_PATH = path.join(ROOT_DIR, "package.json");
const RELEASE_ROOT_DIR = path.join(ROOT_DIR, "release");
const RELEASE_DIR = path.join(RELEASE_ROOT_DIR, "PosPrintAgent");

function readVersion() {
  const packageJson = JSON.parse(fs.readFileSync(PACKAGE_JSON_PATH, "utf-8"));
  return packageJson.version;
}

// Shells out to PowerShell's Compress-Archive (present on every Windows
// machine this project targets) instead of adding a zip npm dependency -
// this project deliberately keeps its dependency list to express/zod (see
// package.json), and this is the simplest thing that works without adding
// one. The release folder is entirely generated by this project's own
// scripts, so there are no stray junk files (Thumbs.db, .DS_Store, ...) to
// filter out.
function createZip(version) {
  if (!fs.existsSync(RELEASE_DIR)) {
    throw new Error(`Release folder not found: ${RELEASE_DIR}\nRun "npm run release:win" (or "npm run package:win" + "npm run prepare:release") first.`);
  }

  fs.mkdirSync(RELEASE_ROOT_DIR, { recursive: true });
  const zipPath = path.join(RELEASE_ROOT_DIR, `PosPrintAgent-v${version}.zip`);

  if (fs.existsSync(zipPath)) {
    fs.rmSync(zipPath);
  }

  const sourceGlob = path.join(RELEASE_DIR, "*");
  const command = `Compress-Archive -Path '${sourceGlob}' -DestinationPath '${zipPath}' -Force`;
  execFileSync("powershell.exe", ["-NoProfile", "-NonInteractive", "-Command", command], { stdio: "inherit" });

  return zipPath;
}

function main() {
  const version = readVersion();
  const zipPath = createZip(version);
  console.log("");
  console.log(`Release zip created: ${zipPath}`);
}

main();
